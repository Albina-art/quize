const seedQuestions = [
  ...corsQuestions,
  ...httpsTlsQuestions,
  ...browserPipelineQuestions,
];

async function main() {
  for (const row of seedQuestions) {
    const existing = await prisma.question.findFirst({
      where: { topic: row.topic, question: row.question },
    });
    if (!existing) {
      await prisma.question.create({ data: row });
    }
  }
}

main()
  .then(async () => {
    await prisma.$disconnect();
  })
  .catch(async (e) => {
    if (e instanceof Prisma.PrismaClientKnownRequestError && e.code === "P2021") {
      console.error(`
Таблица в базе ещё не создана. Сначала примените схему Prisma, затем повторите сид:

  npx prisma migrate dev

или без файлов миграций (только синхронизация схемы):

  npx prisma db push

После этого снова: npm run db:seed
`);
    } else {
      console.error(e);
    }
    await prisma.$disconnect();
    process.exit(1);
  });
